#!/bin/bash 

# -----------------------------------------------------------------------------
# Check if already installed
# Return 0 if installed, 1 otherwise
# -----------------------------------------------------------------------------
function lynx_is_installed {
	apt_is_installed lynx-cur
}

# -----------------------------------------------------------------------------
# Install
# -----------------------------------------------------------------------------
function lynx_uninstall {
	apt_purge lynx-cur
}

# -----------------------------------------------------------------------------
# Install
# -----------------------------------------------------------------------------
function lynx_install {
	apt_force_install lynx-cur
}

